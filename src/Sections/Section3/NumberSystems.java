package Sections.Section3;

public class NumberSystems {
    public static void main(String[] args) {
        // Decimal format
        int dec = 152;
        // Binary format
        int bin = 0b10011000;
        // Octal format
        int oct = 0230;
        // Hexadecimal format
        int hex = 0x98;

        // Todas essas variáveis representam o mesmo número em formato decimal.
        System.out.println(dec);
        System.out.println(bin);
        System.out.println(oct);
        System.out.println(hex);
    }
}
